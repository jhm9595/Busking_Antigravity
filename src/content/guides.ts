export function getAllGuides(): GuideEntry[] {
  return guides;
}

export function getGuideBySlug(slug: string): GuideEntry | undefined {
  return guides.find((guide) => guide.slug === slug);
}

export function getRelatedGuides(guide: GuideEntry, limit = 3): GuideEntry[] {
  const explicit = guide.relatedSlugs
    .map((slug) => getGuideBySlug(slug))
    .filter((entry): entry is GuideEntry => Boolean(entry));

  if (explicit.length >= limit) {
    return explicit.slice(0, limit);
  }

  const fallback = guides.filter(
    (candidate) => candidate.slug !== guide.slug && !guide.relatedSlugs.includes(candidate.slug),
  );

  return [...explicit, ...fallback].slice(0, limit);
}
